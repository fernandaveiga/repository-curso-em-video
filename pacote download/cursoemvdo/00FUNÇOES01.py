# def é o comando que armazena uma função, uma função que é rotina no seu programa
# abaixo um exemplo onde o def linha armazena o print com as 30 linhas.
def linha():
    print('-' * 30)


# programa principal
linha()
print('     CURSO EM VÍDEO     ')
linha()
print('     APRENDA PYTHON     ')
linha()
print('     GUSTAVO GUANABARA     ')
linha()

